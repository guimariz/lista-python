# Faça um Programa que peça as 4 notas bimestrais e mostre a média.
# Entrada: 4 notas
# saida: media das 4 notas

nota_bim = int(input("Informe 1a a nota: "))
nota_bim2 = int(input("Informe 2a a nota: "))
nota_bim3 = int(input("Informe 3a a nota: "))
nota_bim4 = int(input("Informe 4a a nota: "))

media = (nota_bim + nota_bim2 + nota_bim3 + nota_bim4) / 4

print(media)